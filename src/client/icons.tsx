export type PixelIconName = keyof typeof GLYPHS;

export function PixelIcon({ name, size = 16 }: { name: PixelIconName; size?: number }): ReactNode {
  return (
    <svg width={size} height={size} viewBox="0 0 8 8" shapeRendering="crispEdges" aria-hidden="true" focusable="false">
      {GLYPHS[name].flatMap((row, y) =>
        Array.from(row, (cell, x) =>
          cell === "#" ? <rect key={`${x}.${y}`} x={x} y={y} width={1} height={1} fill="currentColor" /> : null
        )
      )}
    </svg>
  );
}
